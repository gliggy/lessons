public class Main {
public static void main(String[] args) {
    int x = 3;
    switch (x) {
        case 2:
            System.out.println("x is two!");
            break;
        case 4:
            System.out.println("x is four!!");
            break;
        case 1:
            System.out.println("x is one!!!");
            break;
        default:
            System.out.println("I don't care about x");
    }
}
}
